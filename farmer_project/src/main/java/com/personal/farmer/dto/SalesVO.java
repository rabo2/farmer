package com.personal.farmer.dto;

public class SalesVO {
	
	private String sal_no;
	private String ship_no;
	private int slaes_price;
	private String sal_check;
	private String buy_no;
	private String sal_date;

	public String getSal_no() {
		return sal_no;
	}
	public void setSal_no(String sal_no) {
		this.sal_no = sal_no;
	}
	public String getShip_no() {
		return ship_no;
	}
	public void setShip_no(String ship_no) {
		this.ship_no = ship_no;
	}
	public int getSlaes_price() {
		return slaes_price;
	}
	public void setSlaes_price(int slaes_price) {
		this.slaes_price = slaes_price;
	}
	public String getSal_check() {
		return sal_check;
	}
	public void setSal_check(String sal_check) {
		this.sal_check = sal_check;
	}
	public String getBuy_no() {
		return buy_no;
	}
	public void setBuy_no(String buy_no) {
		this.buy_no = buy_no;
	}
	public String getSal_date() {
		return sal_date;
	}
	public void setSal_date(String sal_date) {
		this.sal_date = sal_date;
	}
	
	
	
}
