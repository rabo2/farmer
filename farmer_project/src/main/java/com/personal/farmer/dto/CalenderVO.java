package com.personal.farmer.dto;

public class CalenderVO {
	private String cal_no;
	private String mem_id;
	private String cal_date;
	private String cal_title;
	private String cal_content;
	private int cal_rank;
	private String cal_check;
	
	public String getCal_no() {
		return cal_no;
	}
	public void setCal_no(String cal_no) {
		this.cal_no = cal_no;
	}
	public String getMem_id() {
		return mem_id;
	}
	public void setMem_id(String mem_id) {
		this.mem_id = mem_id;
	}
	public String getCal_date() {
		return cal_date;
	}
	public void setCal_date(String cal_date) {
		this.cal_date = cal_date;
	}
	public String getCal_title() {
		return cal_title;
	}
	public void setCal_title(String cal_title) {
		this.cal_title = cal_title;
	}
	public String getCal_content() {
		return cal_content;
	}
	public void setCal_content(String cal_content) {
		this.cal_content = cal_content;
	}
	public int getCal_rank() {
		return cal_rank;
	}
	public void setCal_rank(int cal_rank) {
		this.cal_rank = cal_rank;
	}
	public String getCal_check() {
		return cal_check;
	}
	public void setCal_check(String cal_check) {
		this.cal_check = cal_check;
	}
}
