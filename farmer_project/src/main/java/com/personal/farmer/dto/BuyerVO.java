package com.personal.farmer.dto;

public class BuyerVO {	
	private String buy_no;
	private String buy_name;
	private String buy_rep;
	private String buy_tel;
	private String buy_add;
	private int buy_fee;
	private String buy_comment;
	
	public String getBuy_no() {
		return buy_no;
	}
	public void setBuy_no(String buy_no) {
		this.buy_no = buy_no;
	}
	public String getBuy_name() {
		return buy_name;
	}
	public void setBuy_name(String buy_name) {
		this.buy_name = buy_name;
	}
	public String getBuy_rep() {
		return buy_rep;
	}
	public void setBuy_rep(String buy_rep) {
		this.buy_rep = buy_rep;
	}
	public String getBuy_tel() {
		return buy_tel;
	}
	public void setBuy_tel(String buy_tel) {
		this.buy_tel = buy_tel;
	}
	public String getBuy_add() {
		return buy_add;
	}
	public void setBuy_add(String buy_add) {
		this.buy_add = buy_add;
	}
	public int getBuy_fee() {
		return buy_fee;
	}
	public void setBuy_fee(int buy_fee) {
		this.buy_fee = buy_fee;
	}
	public String getBuy_comment() {
		return buy_comment;
	}
	public void setBuy_comment(String buy_comment) {
		this.buy_comment = buy_comment;
	}
	
	
}
